function fundacionPrimera(arr) {

    let dbCSV = arr.map(value => [value, 'X']);
    for (let inicio1 = 0; inicio1 <= 20; inicio1++) {
        var numeroBase = dbCSV[inicio1][0];
        console.log(numeroBase)
        for (let inicio11 = inicio1; inicio11 < dbCSV.length; inicio11++) {
            if (dbCSV[inicio11][1] == 'X') {
                var numeroCopia = dbCSV[inicio11][0];
                dbCSV[inicio11][1] = inicio11;
                if (numeroBase == numeroCopia) {
                    console.log("SON IGUALES: ", numeroCopia)
                    dbCSV[inicio11][1] = inicio1;
                } else {
                    dbCSV[inicio11][1] = 'X';
                }
            } else {
                console.log("Número ya revisado: ", dbCSV[inicio11][0])
            }
        }
        console.log(dbCSV);
    }
    //return null; // No se encontraron partes idénticas
}

